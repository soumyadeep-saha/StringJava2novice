package com.soumyadeep.string;

public class ChangeCase {

	public static void main(String[] args) {

		/*
		 * Below example shows how to convert the case of a given string. toUpperCase()
		 * method converts all string characters to upper case. toLowerCase() method
		 * converts all string characters to lower case
		 */
        String str = "Change My Case";
        System.out.println("Upper Case: "+str.toUpperCase());
        System.out.println("Lower Case: "+str.toLowerCase());
	}
}
