package com.soumyadeep.string;

public class TrimSpaces {

	public static void main(String[] args) {

		/*
		 * Below example shows how to trim spaces in the given string. The trim()
		 * function removes all kind of space characters at both ends, means removes
		 * starting and trailing spaces. These space characters includes normal space,
		 * enter, new line, tab, etc
		 */
        String str = "  Junk   ";
        System.out.println(str.trim());
	}
}
